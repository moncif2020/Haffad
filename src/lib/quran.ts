export const getAudioUrl = (reciterCode: string, surahNum: number | null, ayahInSurah: number | null, mirrorIndex: number = 0) => {
  if (surahNum === null || ayahInSurah === null) return "";
  const surahStr = surahNum.toString().padStart(3, '0');
  const ayahStr = ayahInSurah.toString().padStart(3, '0');
  
  // Handle full URL reciters (from mp3quran.net etc)
  if (reciterCode.startsWith('http')) {
    // These usually follow a pattern like baseUrl/100001.mp3
    return `${reciterCode}${surahStr}${ayahStr}.mp3`;
  }

  const mirrors = [
    `https://everyayah.com/data/${reciterCode}/${surahStr}${ayahStr}.mp3`,
    `https://www.everyayah.com/data/${reciterCode}/${surahStr}${ayahStr}.mp3`,
    `https://mirrors.quranicaudio.com/everyayah/data/${reciterCode}/${surahStr}${ayahStr}.mp3`
  ];

  return mirrors[mirrorIndex % mirrors.length];
};

async function fetchWithRetry(url: string, options: RequestInit = {}, retries = 2): Promise<Response> {
  try {
    const response = await fetch(url, options);
    if (!response.ok) throw new Error(`HTTP error! status: ${response.status}`);
    return response;
  } catch (error) {
    if (retries > 0) {
      // Exponential backoff
      await new Promise(resolve => setTimeout(resolve, 1000 * (3 - retries)));
      return fetchWithRetry(url, options, retries - 1);
    }
    throw error;
  }
}

export async function safeJson(response: Response) {
  const text = await response.text();
  if (!text) throw new Error("Empty response body");
  try {
    return JSON.parse(text);
  } catch (e) {
    console.error("JSON parse error. Body snippet:", text.slice(0, 200));
    throw e;
  }
}

export const fetchAyahs = async (surahNum: number, from: number, to: number) => {
  try {
    // Primary source: alquran.cloud
    try {
      const response = await fetchWithRetry(`https://api.alquran.cloud/v1/surah/${surahNum}/editions/quran-uthmani`);
      const data = await safeJson(response);
      const surah = data.data?.[0];
      if (!surah) throw new Error("Invalid data structure from alquran.cloud");
      
      return {
        surahName: surah.name,
        ayahs: surah.ayahs.slice(from - 1, to).map((a: any) => ({
          number: a.number,
          numberInSurah: a.numberInSurah,
          text: a.text
        }))
      };
    } catch (e) {
      console.warn('Primary API failed, trying fallback source...', e);
      // Fallback source: api.quran.com
      const response = await fetchWithRetry(`https://api.quran.com/api/v4/quran/verses/uthmani?chapter_number=${surahNum}`);
      const data = await safeJson(response);
      const surahData = QURAN_SURAHS[surahNum - 1];
      
      if (!data.verses) throw new Error("Invalid data structure from api.quran.com");

      return {
        surahName: surahData.name,
        ayahs: data.verses.slice(from - 1, to).map((v: any) => ({
          number: v.id,
          numberInSurah: parseInt(v.verse_key.split(':')[1]),
          text: v.text_uthmani
        }))
      };
    }
  } catch (error) {
    console.error('Final error fetching ayahs:', error);
    throw error;
  }
};

export const downloadSurahAudio = async (
  surahNum: number, 
  from: number, 
  to: number, 
  reciter: string, 
  onProgress?: (progress: number) => void
) => {
  const total = to - from + 1;
  const cache = await caches.open('quran-audio');
  
  for (let i = from; i <= to; i++) {
    const url = getAudioUrl(reciter, surahNum, i);
    const cached = await cache.match(url);
    if (!cached) {
      try {
        const response = await fetch(url);
        if (response.ok) {
          await cache.put(url, response);
        }
      } catch (e) {
        console.error(`Failed to download ayah ${i} of surah ${surahNum}`, e);
      }
    }
    if (onProgress) {
      onProgress(Math.round(((i - from + 1) / total) * 100));
    }
  }
};

export const isRangeDownloaded = async (surahNum: number | null, from: number | null, to: number | null, reciter: string) => {
  if (surahNum === null || from === null || to === null) return false;
  try {
    const cache = await caches.open('quran-audio');
    for (let i = from; i <= to; i++) {
      const url = getAudioUrl(reciter, surahNum, i);
      const cached = await cache.match(url);
      if (!cached) return false;
    }
    return true;
  } catch (e) {
    console.error("Cache check failed:", e);
    return false;
  }
};

// Cache for local search to ensure accuracy and speed
let fullQuranSearchIndex: any[] | null = null;
let isFetchingIndex = false;

// Helper to normalize Arabic text (remove Tashkeel and unify letters)
export const normalizeArabic = (text: string): string => {
  if (!text) return "";
  
  // Robust Arabic normalization:
  // 1. NFD separates characters from marks
  // 2. \p{M} removes ALL diacritics/decoration marks
  // 3. NFC brings it back to standard form
  return text.normalize('NFD')
    .replace(/\p{M}/gu, '')
    .normalize('NFC')
    .replace(/[أإآٱ]/g, "ا")
    .replace(/ة/g, "ه")
    .replace(/[ىي]/g, "ي")
    .replace(/ؤ/g, "و")
    .replace(/ئ/g, "ي")
    .replace(/[\u060C\u061B\u061F\u06D4۝]/g, "")
    .replace(/[٠-٩0-9]/g, "")
    .trim()
    .replace(/\s+/g, " ");
};

