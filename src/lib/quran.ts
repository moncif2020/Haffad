export const getGlobalAyahNumber = (surahNumber: number, ayahNumberInSurah: number) => {
  let count = 0;
  for (let i = 0; i < surahNumber - 1; i++) {
    count += QURAN_SURAHS[i].numberOfAyahs;
  }
  return count + ayahNumberInSurah;
};

export const fetchAyahs = async (surahNumber: number, fromAyah: number, toAyah: number) => {
  const cacheKey = `quran-surah-${surahNumber}`;
  let data: any = null;
  let usedSecondary = false;

  try {
    const cache = await caches.open('quran-data');
    const cachedRes = await cache.match(cacheKey);
    if (cachedRes) {
      data = await cachedRes.json();
    } else {
      // Try primary API
      const res = await fetch(`https://api.alquran.cloud/v1/surah/${surahNumber}`);
      if (!res.ok) throw new Error('Primary API failed');
      data = await res.json();
      if (data.code !== 200) throw new Error('Primary API returned error code');
      
      // Save to cache
      cache.put(cacheKey, new Response(JSON.stringify(data)));
    }
  } catch (err) {
    console.warn('Primary API failed, falling back to secondary API', err);
    try {
      const cache = await caches.open('quran-data-secondary');
      const cachedRes = await cache.match(cacheKey);
      if (cachedRes) {
        data = await cachedRes.json();
        usedSecondary = true;
      } else {
        // Try secondary API
        const res = await fetch(`https://quranapi.pages.dev/api/${surahNumber}.json`);
        if (!res.ok) throw new Error('Secondary API failed');
        data = await res.json();
        cache.put(cacheKey, new Response(JSON.stringify(data)));
        usedSecondary = true;
      }
    } catch (err2) {
      throw new Error('Both APIs failed and no cache available');
    }
  }

  if (!usedSecondary) {
    const ayahs = data.data.ayahs.filter((a: any) => a.numberInSurah >= fromAyah && a.numberInSurah <= toAyah);
    return {
      surahName: data.data.name,
      ayahs: ayahs.map((a: any) => ({
        text: a.text,
        numberInSurah: a.numberInSurah,
        globalNumber: a.number
      }))
    };
  } else {
    const ayahs = [];
    for (let i = fromAyah; i <= toAyah; i++) {
      ayahs.push({
        text: data.arabic1[i - 1],
        numberInSurah: i,
        globalNumber: getGlobalAyahNumber(surahNumber, i)
      });
    }
    return {
      surahName: data.surahNameArabic,
      ayahs
    };
  }
};

export const downloadSurahAudio = async (surahNumber: number, fromAyah: number, toAyah: number, reciter: string, onProgress: (progress: number) => void) => {
  const data = await fetchAyahs(surahNumber, fromAyah, toAyah);
  const cache = await caches.open('quran-audio');
  
  let downloaded = 0;
  const total = data.ayahs.length;
  
  for (const ayah of data.ayahs) {
    const url = `https://cdn.islamic.network/quran/audio/128/${reciter}/${ayah.globalNumber}.mp3`;
    const cachedRes = await cache.match(url);
    if (!cachedRes) {
      try {
        const res = await fetch(url, { mode: 'no-cors' });
        // For opaque responses (no-cors), res.ok is false and status is 0.
        // We just cache it anyway.
        await cache.put(url, res);
      } catch (e) {
        console.error("Failed to download audio", url, e);
      }
    }
    downloaded++;
    onProgress(Math.round((downloaded / total) * 100));
  }
};